#Slice Data
# Slice a string using 1, 2, and 3 parameters
# Assign string value
text = "Python Programming Language"
 
# Slice using one parameter
sliceObj = slice(5)
print(text[sliceObj])  
 
# Slice using two parameter
sliceObj = slice(6,12)
print(text[sliceObj])  
 
# Slice using three parameter
sliceObj = slice(6,25,5)
print(text[sliceObj])
 
